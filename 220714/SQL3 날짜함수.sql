-- HIRE_DATE가 1987년 6월
SELECT * FROM employees
WHERE HIRE_DATE BETWEEN '1987-06-01' AND '1987-06-30';

SELECT ADDDATE('1987-06-01', 40); -- 날짜 더하는 함수
SELECT SUBDATE('1987-06-01', 1); -- 날짜 빼는 함수
SELECT DATE_ADD('1987-06-01', INTERVAL 40 DAY);  -- 날짜 더하는 함수인데 표현방법이 틀림 DATE_ADD(날짜, INTERVAL 일 DAY)
SELECT DATE_SUB('1987-06-01', INTERVAL 1 MONTH);
SELECT '1987-06-01' + INTERVAL 1 YEAR;
SELECT '1987-06-01' + INTERVAL 1 DAY;

SELECT DAYOFWEEK('2022-07-14'); -- 1이 일요일 2가 월요일 3이 화요일 4가 수요일 5가 목요일

SELECT EXTRACT(YEAR FROM '2022-07-14');
SELECT EXTRACT(MONTH FROM '2022-07-14');
SELECT EXTRACT(DAY FROM '2022-07-14');

SELECT DATEDIFF('2022-06-13', '2022-06-01');

SELECT STR_TO_DATE('2021/06/13', '%Y/%M/%D');
SELECT DATE_FORMAT(DATE '2021-06-13', '%Y/%M/%D');

-- 직원 테이블 17일에 고용된 인원
SELECT * FROM employees
WHERE HIRE_DATE LIKE '%17';

SELECT * FROM employees
WHERE EXTRACT(DAY FROM HIRE_DATE) = 17;

-- job_history 테이블에 각 행별로 일한 기간(일)을 조회
SELECT START_DATE, END_DATE, DATEDIFF(END_DATE, START_DATE) AS "기간" FROM job_history;

